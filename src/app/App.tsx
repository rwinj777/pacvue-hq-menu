// ─── App Shell ────────────────────────────────────────────────────────────────

export default function App() {
  const [navOpen, setNavOpen] = useState(false);
  const [selectedRole, setSelectedRole] = useState<RoleId>("executive");
  const [currentPage, setCurrentPage] = useState<PageId | null>(null);
  const navRef = useRef<HTMLDivElement>(null);

  // Close nav on outside click
  useEffect(() => {
    function handler(e: MouseEvent) {
      if (navRef.current && !navRef.current.contains(e.target as Node)) {
        setNavOpen(false);
      }
    }
    document.addEventListener("mousedown", handler);
    return () => document.removeEventListener("mousedown", handler);
  }, []);

  return (
    <div
      data-canvas
      className="relative overflow-hidden"
      style={{ width: 1440, height: 810 }}
    >
        {/* Background image */}
        <img
          src={bgImage}
          alt=""
          className="absolute inset-0 w-full h-full object-cover object-top pointer-events-none select-none"
        />

        {/* Pacvue HQ trigger button — matches Figma: left-[206px] top-[17px] */}
        <div ref={navRef} className="absolute" style={{ left: 206, top: 17 }}>
          <PacvueHQButton onClick={() => setNavOpen((v) => !v)} isOpen={navOpen} />

          {/* Dropdown panel — appears at top-[47px] per Figma (30px below button top) */}
          {navOpen && (
            <div className="absolute left-0 z-50" style={{ top: 30 }}>
              <PacvueHQDropdown
                selectedRole={selectedRole}
                onRoleChange={(r) => setSelectedRole(r)}
                onNavigate={(p) => setCurrentPage(p)}
                onClose={() => setNavOpen(false)}
              />
            </div>
          )}
        </div>

        {/* Page content overlay */}
        {currentPage && (
          <PageOverlay pageId={currentPage} onClose={() => setCurrentPage(null)} />
        )}
    </div>
  );
}
