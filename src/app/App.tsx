// ─── Tooltip Component ────────────────────────────────────────────────────────

const TIP_WIDTH = 256; // w-64
const TIP_GAP = 10;    // gap between item edge and tooltip box

function Tooltip({ text, children }: { text: string; children: React.ReactNode }) {
  const [pos, setPos] = useState<{ x: number; y: number; side: "right" | "left" } | null>(null);
  const containerRef = useRef<HTMLDivElement>(null);

  const show = () => {
    if (!containerRef.current) return;
    const rect = containerRef.current.getBoundingClientRect();
    const wouldOverflow = rect.right + TIP_GAP + TIP_WIDTH > window.innerWidth - 8;
    const side = wouldOverflow ? "left" : "right";
    setPos({
      x: side === "right" ? rect.right + TIP_GAP : rect.left - TIP_GAP - TIP_WIDTH,
      y: rect.top + rect.height / 2,
      side,
    });
  };

  return (
    <div
      ref={containerRef}
      onMouseEnter={show}
      onMouseLeave={() => setPos(null)}
    >
      {children}
      {pos && createPortal(
        <div
          className="fixed z-[9999] pointer-events-none"
          style={{ left: pos.x, top: pos.y, width: TIP_WIDTH, transform: "translateY(-50%)" }}
        >
          {/* Arrow */}
          <div
            className={`absolute top-1/2 -translate-y-1/2 size-0 border-y-[6px] border-y-transparent ${
              pos.side === "right"
                ? "-left-[7px] border-r-[8px] border-r-[#323232]"
                : "-right-[7px] border-l-[8px] border-l-[#323232]"
            }`}
          />
          <div className="bg-[#323232] text-white text-xs leading-relaxed rounded-lg px-3 py-2.5 shadow-xl">
            {text}
          </div>
        </div>,
        document.body
      )}
    </div>
  );
}

